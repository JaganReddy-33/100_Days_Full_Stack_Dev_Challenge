
// Comparison Operator

console.log(10 == "10");
console.log(10 === "10");
console.log(10 != 5);
console.log(10 !==  "10");
console.log(20 > 15);
console.log(20 < 15);


// Check if two numbers are equal using ==.
let a = 10;
let b = "10";
console.log(a == b);

// Check if two values are strictly equal using ===.
let x = 20;
let y = "20";
console.log(x === y);


// Check if two strings are different.
let city1 = "Hyderabad";
let city2 = "Bangalore";
console.log(city1 != city2);  // check's value only
console.log(city1 !== city2); // check's both value and type


// Check if marks are greater than passing marks.
let marks = 82;
let passMarks = 35;
console.log("is Student pass? : ",marks > passMarks);

// Check if salary is less than expected salary.
let salary = 2500;
let expectedSalary = 5000;
console.log("is expected salary less than salary? ", salary < expectedSalary);

// Check whether temperature is greater than or equal to 40.
let temp = 45;
console.log(temp >= 40);

// Check if a student's age is less than or equal to 18.
let age = 21;
