//!----- Using `var`----
var myAge = 30; //declared
myAge = 20; //Re-assign
console.log(myAge);

var studentName = "Jagan";
console.log(studentName);

studentName = "Ajay";
console.log(studentName);

var mobilePrice = 25000;
mobilePrice = 27000;
console.log(mobilePrice);

var temperature = 32;
console.log(temperature);
temperature = 28;
console.log(temperature);


//!-----Using `let`----
let myScore = 10;
myScore = 50;  //Re-assign
console.log(myScore);

let bankBalance = 5000;
console.log(bankBalance);

bankBalance = 8000;
console.log(bankBalance);

let totalItems = 3;
console.log(totalItems);
totalItems = 5;
console.log(totalItems);

let score = 120;
score = score + 6;
console.log(score);

let username = "jagan123";
console.log(username);
username = "jmr_dev";
console.log(username);

let isLoggedIn = false;
console.log(isLoggedIn);
isLoggedIn = true;
console.log(isLoggedIn);

let semester = 7;
console.log(semester);
semester = 8;
console.log(semester);


