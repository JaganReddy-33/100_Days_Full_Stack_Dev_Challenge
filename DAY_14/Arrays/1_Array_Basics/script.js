
//  Array basics

// Create an array
let arr = [1, 2, 3, 4];
console.log(arr);


// Print first element
let arr1 = ["apple", "banana", "orange", "guava"];
console.log(arr1[0]);


// Print last element
let arr2 = [11, 12, 13, 14, 15];
console.log(arr2[arr2.length-1]);

// Replace element
let arr3 = ["Jagan", 3, 45, "Jmr", "apple", true];
arr3[3] = "Ajay";
console.log(arr3);

// Print length
let arr4 = [1, 2, 3, 4, 5, 6, 7, 8, 9];
let len = arr4.length;
console.log(len);

// Nested array
let matrix = [[1, 2], [3, 4], [5, 6]];
console.log(matrix);


// Mixed array
let mixed = ['text', 42, true, null];
console.log(mixed);


// Student names
let names = ["Jmr", "Ajay", "lohith", "DSP", "santhosh"];
console.log(names);

// Favorite movies
let movies = ['Harry Potter', 'Avengers', 'Iron man', 'Games of thrones', 'Stranger things'];
console.log(movies);


// Cities
let cities = ['India','Tokyo', 'Paris', 'New York', 'USA', 'china'];
console.log(cities);
